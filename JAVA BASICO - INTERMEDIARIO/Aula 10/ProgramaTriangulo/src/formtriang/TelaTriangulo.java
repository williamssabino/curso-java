    private void btnTriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTriActionPerformed
        // TODO add your handling code here:
        int a = sldA.getValue();
        int b = sldB.getValue();
        int c = sldC.getValue();
        panResult.setVisible(true);
        
        if ((a<(b+c)) || (b<(a+c)) || (c<(b+a))){
            lblTri.setText("Forma um Triangulo");
            
            if ((a!=b)&&(a!=c)&&(b!=c)){
                panResult2.setVisible(true);
                lblResult2.setText("Escaleno");
                
            }
            else if ((a==b)&&(b==c)){
               panResult2.setVisible(true);
               lblResult2.setText("Equilatero");
            }
            else{
                panResult2.setVisible(true);
                lblResult2.setText("Isoceles");
            }
        }
        if(a<=0 || b<=0 || c<=0){
            lblTri.setText("N√£o forma um triangulo");
            panResult2.setVisible(false);
        }
        if ((a>(b+c)) || (b>(a+c)) || (c>(b+a))){
            lblTri.setText("N√£o Forma um Triangulo");
            panResult2.setVisible(false);
            
        }
    }//GEN-LAST:event_btnTriActionPerformed

